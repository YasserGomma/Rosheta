package com.example.rosheta.data.models.remote;

public class User {
    public int id;
    public String name;
    public String national_id;
    public String phone;
    public int role_id;
    public String email;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getNational_id() {
        return national_id;
    }

    public String getPhone() {
        return phone;
    }

    public int getRole_id() {
        return role_id;
    }

    public String getEmail() {
        return email;
    }
}
